import { chromium } from "playwright-core";
const SCRATCH = process.argv[2];
const browser = await chromium.launch({ channel: "chrome", headless: true });
const page = await browser.newPage({ viewport: { width: 390, height: 844 }, deviceScaleFactor: 2 });
await page.goto("http://localhost:3000/speaking-test", { waitUntil: "networkidle" });
await page.getByText("متوسط", { exact: false }).first().click();
await page.waitForTimeout(600);
await page.screenshot({ path: `${SCRATCH}/flow-read.png` });
await page.getByRole("button", { name: /أنا جاهز للتسجيل/ }).click();
await page.waitForTimeout(500);
await page.getByRole("button", { name: /ابدأ التسجيل/ }).click();
await page.waitForTimeout(900);
await page.screenshot({ path: `${SCRATCH}/flow-recording.png` });
await page.waitForTimeout(1400);
await page.screenshot({ path: `${SCRATCH}/flow-popup.png` });
const waHref = await page.locator('a:has-text("أرسل عبر واتساب")').getAttribute("href");
console.log("WA href:", waHref === "#" ? "# (TODO number — dev badge shown ✓)" : waHref.slice(0, 60));
await browser.close();
console.log("✓ flow verified: level → read → record → popup");
